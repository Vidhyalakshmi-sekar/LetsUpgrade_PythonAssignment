# Getting inputs from the users

first_number = int(input("Enter your first number: "))
second_number = int(input("Enter your second number: "))

add = first_number + second_number
sub = first_number - second_number
mul = first_number * second_number
float_div = first_number / second_number
int_div = first_number // second_number

# float_div = Floating point Division
# int_div = Integer Division

print("Our Addition is", add)
print("Our Subtraction is", sub)
print("Our Multiplication is", mul)
print("Our Float point division is", float_div)
print("Our Integer division is", int_div)
